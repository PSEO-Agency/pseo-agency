// Helper function to get FAQs for a specific service
export const getServiceFAQs = (serviceTitle: string, serviceSlug?: string) => {
  // Try to match by slug first, then by title
  const slug = serviceSlug || serviceTitle.toLowerCase().replace(/\s+/g, '-');
  
  // Direct slug matches
  if (serviceFAQs[slug as keyof typeof serviceFAQs]) {
    return serviceFAQs[slug as keyof typeof serviceFAQs];
  }
  
  // Fuzzy matching for common variations
  const title = serviceTitle.toLowerCase();
  
  if (title.includes('programmatic') && title.includes('seo')) {
    return serviceFAQs['programmatic-seo'];
  }
  if (title.includes('seo') && title.includes('automation')) {
    return serviceFAQs['seo-automation'];
  }
  if (title.includes('ai') && title.includes('website')) {
    return serviceFAQs['ai-website'];
  }
  if (title.includes('ai') && title.includes('content')) {
    return serviceFAQs['ai-content-automation'];
  }
  if (title.includes('webscraping') || title.includes('web scraping')) {
    return serviceFAQs['webscraping'];
  }
  if (title.includes('technical') && title.includes('seo')) {
    return serviceFAQs['technical-seo'];
  }
  if ((title.includes('pr') || title.includes('link')) && title.includes('building')) {
    return serviceFAQs['pr-linkbuilding'];
  }
  if (title.includes('analytics') || title.includes('reporting')) {
    return serviceFAQs['analytics-reporting'];
  }
  
  // Return null if no match found (component will use default FAQs)
  return null;
};
